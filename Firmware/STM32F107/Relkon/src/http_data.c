unsigned char get_webdyn(flist* fdyn,unsigned short offset)
{
	unsigned short i,j;
	unsigned char n10=0,n100=0,val;
	switch(fdyn->name[0])
	{
		case 'd':
			i = offset/4;
			j = offset%4;
			switch(i)
			{
				case 0:val = _Sys_IN[0];break;
				case 1:val = _SysRealTmr >> 8;break;
				case 2:val = _SysRealTmr & 0xFF;break;
				default:val=0;break;
			}
			n10=n100=0;val = _Sys.Mem.b1[i];
			while(val>=100){val-=100;n100++;}
			while(val>=10){val-=10;n10++;}
			switch(j)
			{
				case 0:return(n100+'0');
				case 1:return(n10+'0');
				case 2:return(val+'0');
				case 3:return(',');
			}
			break;
	}
	return 0;
}
